/**
 * Extends Coin class. This is a coin of type Penny with value 1 cent.
 * @author Saul
 * @author Abel
 */
public class Penny extends Coin{
	/**
	 * Default constructor for the Penny class. Value attribute (inherited from the Coin class) is set equal to the
	 * 1
	 */
	public Penny() {
		this.value = 1;
	}
}
